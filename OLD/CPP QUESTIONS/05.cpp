/*WAP to Find the Size of int, float, double and char.*/

#include <iostream>
using namespace std;

int main()
{
    int a;
    float b;
    char c;

    cout << "Size of int: " << sizeof(a) << endl;
    cout << "Size of float: " << sizeof(b) << endl;
    cout << "Size of character: " << sizeof(c) << endl;

    return 0;
}